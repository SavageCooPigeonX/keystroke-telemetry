import json, sys, os, traceback
from pathlib import Path

PROJECT_ROOT = Path(__file__).parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

from pigeon_compiler.weakness_planner.deepseek_plan_prompt_seq004_v003_d0314__build_and_send_deepseek_cut_lc_desc_upgrade import (
    build_plan_prompt, request_cut_plan)

COMPILER_OUT = Path(__file__).parent / "compiler_output"
TARGETS = ["folder_auditor", "master_auditor"]


def load_ether_map(name: str) -> dict:
    path = COMPILER_OUT / f"{name}_ether_map.json"
    return json.loads(path.read_text(encoding='utf-8'))


def load_source(name: str) -> str:
    return (PROJECT_ROOT / "codebase_auditor" / f"{name}.py").read_text(
        encoding='utf-8')


def run_deepseek_plans():
    results = {}
    for name in TARGETS:
        print(f"\n{'='*60}")
        print(f"  DEEPSEEK CUT PLAN: {name}")
        print(f"{'='*60}")
        em = load_ether_map(name)
        src = load_source(name)
        prompt = build_plan_prompt(em, src)
        print(f"  Prompt length: {len(prompt)} chars")
        print(f"  Sending to DeepSeek...")
        try:
            resp = request_cut_plan(em, src)
            out = COMPILER_OUT / f"{name}_cut_plan.json"
            out.write_text(json.dumps(resp, indent=2, default=str),
                           encoding='utf-8')
            content = resp.get("response", {}).get("content", "")
            print(f"  Response length: {len(content)} chars")
            print(f"  Saved: {out}")
            results[name] = resp
        except Exception as e:
            print(f"  ERROR: {e}")
            traceback.print_exc()
    return results


if __name__ == "__main__":
    print("=" * 60)
    print("  PIGEON COMPILER — Phase 2: DeepSeek Cut Plans")
    print("=" * 60)
    results = run_deepseek_plans()
    print(f"\n✓ Phase 2 complete — {len(results)} cut plans received")
